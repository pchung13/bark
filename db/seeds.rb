#   shell.say name
#
student_id  = shell.ask "Which student id do you want use for logging into admin?"
password    = shell.ask "Tell me the password to use:"

shell.say ""

account = Account.create(:student_id => student_id, :password => password, :password_confirmation => password, :role => "admin")

if account.valid?
  shell.say "================================================================="
  shell.say "Account has been successfully created, now you can login with:"
  shell.say "================================================================="
  shell.say "   student_id: #{student_id}"
  shell.say "   password: #{password}"
  shell.say "================================================================="
else
  shell.say "Sorry but some thing went wrong!"
  shell.say ""
  account.errors.full_messages.each { |m| shell.say "   - #{m}" }
end

shell.say ""
